export function buildSystemPrompt(extra?: string, team?: 'mavericks' | 'wings'): string {
  const teamContext = team === 'wings'
    ? `\n\n## 컨텍스트: WNBA 댈러스 윙스 글\n- 이 글은 WNBA 댈러스 윙스에 관한 것이다.\n- "댈러스 매버릭스 팬덤" 같은 표현 대신 "윙스 팬덤" 또는 "댈러스 농구 팬들이 함께 응원하는 윙스" 같은 표현을 사용한다.\n- WNBA 팀과 NBA 팀을 혼동하지 않는다.\n- 매버릭스 선수/감독을 윙스 글에 끌어들이지 않는다.`
    : team === 'mavericks'
    ? `\n\n## 컨텍스트: NBA 댈러스 매버릭스 글\n- 이 글은 NBA 댈러스 매버릭스에 관한 것이다.\n- 출처에 명시되지 않은 선수/감독 이름을 추가하지 않는다.`
    : '';
  const tail = extra ? `\n\n## 추가 지시\n${extra}` : '';
  return VOICE_SPEC_INLINE + teamContext + tail;
}
